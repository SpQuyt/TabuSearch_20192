package algorithm;

import java.util.Random;

public class DataStructure {
    private int a, b, c;

    public DataStructure(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getC() {
        return c;
    }
}
